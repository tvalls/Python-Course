#aspas simples
print('Hello, world!')
#aspas duplas
print("Hello, world!")
#escape
#para incluir aspas como string e não código suar barra invertida
print("Hello, \"World\"!")
#r mostra caractere de escape
print(r"Hello \"World\"")
#para mostrar aspas posso alternar entre simples e dupla
print('Hello, "World!"')
print("Hello, 'World'!")
