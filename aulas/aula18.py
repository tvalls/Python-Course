#conversão de tipos, coerção
#type convertion, typecasting, coercion
#tipos imutáveis: str, int, float, bool
print(1+1) #operador + somando int
print('a'+'b') #operador + concatenando str
print(int('1')+1) #apesar de '1' estar entre aspas (sendo string) a função int converte a str em int
print(float('1')+1) #float converte para float
print(bool('')) #bool com conteúdo vazio retorna False, qualquer conteúdo retorna True
print(str(11)+'b')


